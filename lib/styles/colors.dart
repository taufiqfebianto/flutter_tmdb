import 'dart:ui';

class ColorStyle {
  Color darkBlue() {
    return const Color(0xff0d253f);
  }

  Color lightBlue() {
    return const Color(0xff01b4e4);
  }

  Color lightGreen() {
    return const Color(0xff90cea1);
  }

  Color transparent() {
    return const Color(0x00ffffff);
  }

  Color white() {
    return const Color(0xFFFFFFFF);
  }

  Color black() {
    return const Color(0xFF000000);
  }
}

final colorStyle = ColorStyle();
